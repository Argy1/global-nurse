import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

const SYSTEM_PROMPT = `You are Global Paro's friendly AI assistant, helping nurses explore international career opportunities.

Key facts about Global Paro:
- We provide guidance and support for nurses looking to work abroad (USA, Canada, Singapore, and more)
- We help with English proficiency preparation (IELTS, OET), licensing (NCLEX, CGFNS), and job placement guidance
- We do NOT guarantee employment outcomes — we provide guidance and support only
- Our Quickstart Guide at /quickstart covers the full process step by step
- Nurses can register at /register to start their journey
- We have WhatsApp community groups for peer support

Guidelines:
- Be warm, encouraging, and professional
- Answer in the same language the user writes in (English, Bahasa Indonesia, etc.)
- Keep responses concise (2-4 sentences unless more detail is needed)
- When relevant, suggest visiting specific pages: /register, /quickstart, /help
- If a question is beyond your knowledge, suggest the user speak with a human support agent
- Never make promises about visa approvals, job guarantees, or specific timelines
- Always include the disclaimer that outcomes vary and depend on individual circumstances when discussing career prospects`;

serve(async (req) => {
  if (req.method === "OPTIONS") {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [
            { role: "system", content: SYSTEM_PROMPT },
            ...messages,
          ],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Terlalu banyak permintaan, coba lagi nanti." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "Layanan AI sedang tidak tersedia. Silakan hubungi support." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(
        JSON.stringify({ error: "Gagal menghubungi AI" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("chat error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
